/**
 * Enumeration that indicates a direction.
 */
public enum Orientation {
	/**
	 * Horizontal.
	 */
	Horizontal,
	/**
	 * Vertical.
	 */
	Vertical;

	/**
	 * Returns the numerical representation of this enum.
	 */
	public int getValue() {
		return ordinal();
	}
}
