package eu.webtoolkit.jwt;

/**
 * Enumeration that indicates a sort order.
 */
public enum SortOrder {
	/**
	 * Ascending sort order.
	 */
	AscendingOrder,
	/**
	 * Descending sort order.
	 */
	DescendingOrder;

	/**
	 * Returns the numerical representation of this enum.
	 */
	public int getValue() {
		return ordinal();
	}
}
